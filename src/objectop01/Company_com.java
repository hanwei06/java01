package objectop01;

public class Company_com {
	
	private String comAdd;
	private int comMoney;
	private String comName;
	private String comStyle;
	
	
	public String getComStyle() {
		return comStyle;
	}
	public void setComStyle(String comStyle) {
		this.comStyle = comStyle;
	}
	
	
	
	public String getComAdd() {
		return comAdd;
	}
	public void setComAdd(String comAdd) {
		this.comAdd = comAdd;
	}
	public int getComMoney() {
		return comMoney;
	}
	public void setComMoney(int comMoney) {
		this.comMoney = comMoney;
	}
	public String getComName() {
		return comName;
	}
	public void setComName(String comName) {
		this.comName = comName;
	}


}
