package objectop01;

public class Company_pos {
	
	private String posName;
	private String posFunction;
	private int num;
	
	
	public String getPosName() {
		return posName;
	}
	public void setPosName(String posName) {
		this.posName = posName;
	}
	public String getPosFunction() {
		return posFunction;
	}
	public void setPosFunction(String posFunction) {
		this.posFunction = posFunction;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}

}
